#include "malloc.h"
#define FT_HEX "ABCDEF"

void				ft_puthex(size_t n)
{
	char	c;

	if (n > 15)
	{
		ft_puthex(n / 16);
		ft_puthex(n % 16);
	}
	else
	{
		c = (n < 10) ? '0' + n : FT_HEX[n % 10];
		write(1, &c, 1);
	}
}
